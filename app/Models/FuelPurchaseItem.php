<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Model;

use Illuminate\Database\Eloquent\Relations\BelongsTo;


class FuelPurchaseItem extends Model
{


    protected $fillable = [


        /*
        |--------------------------------------------------------------------------
        | Purchase
        |--------------------------------------------------------------------------
        */

        'fuel_purchase_id',



        /*
        |--------------------------------------------------------------------------
        | Fuel Information
        |--------------------------------------------------------------------------
        */

        'fuel_type_id',

        'tank_id',



        /*
        |--------------------------------------------------------------------------
        | Quantity
        |--------------------------------------------------------------------------
        */

        'quantity',

        'rate',

        'total',



        /*
        |--------------------------------------------------------------------------
        | Other
        |--------------------------------------------------------------------------
        */

        'remarks',


    ];





    protected $casts = [


        'quantity' => 'decimal:2',

        'rate' => 'decimal:2',

        'total' => 'decimal:2',


    ];







    /*
    |--------------------------------------------------------------------------
    | Fuel Purchase
    |--------------------------------------------------------------------------
    */

    public function fuelPurchase(): BelongsTo
    {

        return $this->belongsTo(
            FuelPurchase::class
        );

    }









    /*
    |--------------------------------------------------------------------------
    | Fuel Type
    |--------------------------------------------------------------------------
    */

    public function fuelType(): BelongsTo
    {

        return $this->belongsTo(
            FuelType::class
        );

    }









    /*
    |--------------------------------------------------------------------------
    | Tank
    |--------------------------------------------------------------------------
    */

    public function tank(): BelongsTo
    {

        return $this->belongsTo(
            Tank::class
        );

    }


}